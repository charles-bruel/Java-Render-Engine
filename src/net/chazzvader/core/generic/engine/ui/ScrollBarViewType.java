package net.chazzvader.core.generic.engine.ui;

/**
 * A simple enum stating where the scroll bar will be rendered.
 * @author csbru
 * @version 1
 * @since 1.0
 */
public enum ScrollBarViewType {
	
	/**
	 * Renders the scroll bar on the left.
	 */
	LEFT,
	/**
	 * Renders the scroll bar on the right.
	 */
	RIGHT,
	/**
	 * Doesn't render the scroll bar at all.
	 */
	INVISIBLE;

}
